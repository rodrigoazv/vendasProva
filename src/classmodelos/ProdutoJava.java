package classmodelos;

public class ProdutoJava {
    private int         codigo;
    private String      nome;
    private int         qnt;
    private float       valorU;

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getQnt() {
        return qnt;
    }

    public void setQnt(int qnt) {
        this.qnt = qnt;
    }

    public float getValorU() {
        return valorU;
    }

    public void setValorU(float valorU) {
        this.valorU = valorU;
    }
    
}
